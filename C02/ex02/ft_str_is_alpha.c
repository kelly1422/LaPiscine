int	ft_str_is_alpha(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if (122 < str[i] || str[i] < 65)
			return (0);
		if (90 < str[i] && str[i] < 97)
			return (0);
		i++;
	}
	return (1);
}
